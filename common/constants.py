@staticmethod
def generate_conversation_id(seq: int) -> str:
    """
    生成会话ID
    :param seq: 序列号
    :return: conv-yyyyMMdd-seq
    """
    date_str = DateUtil.local_date_to_string(DateUtil.get_now_date(), DateUtil.YMD)
    return f"conv-{date_str}-{seq}"

